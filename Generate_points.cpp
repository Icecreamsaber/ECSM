void write_data(string name, int num, mpz_class range){
	unsigned long int seed;
	mpz_class random_num;
	mpz_class sym;
	ofstream f(name);
	seed = time(NULL);
	gmp_randclass r(gmp_randinit_default);
	r.seed(seed);
	for(int i = 0; i < num; i++){
		random_num = r.get_z_range(range / 2 + 1);
		sym = r.get_z_range(2);
		random_num = pow(-1, sym.get_str(2)[0]-'0') * random_num; 
		//cout<<random_num;
		//cout<<endl;
		f<<random_num<<" ";
	}
	f.close();
	return;
}


